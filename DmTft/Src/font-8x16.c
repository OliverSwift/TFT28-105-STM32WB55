int getGlyphIndex(unsigned char c) {
    int index = 0;

    if (c >= LOWERSET_FIRST_CODE && c <= LOWERSET_LAST_CODE) {
        index = c - LOWERSET_FIRST_CODE;
    } else if (c >= UPPERSET_FIRST_CODE) {
        index = c - UPPERSET_FIRST_CODE + (LOWERSET_LAST_CODE - LOWERSET_FIRST_CODE +1);
    }
    return index;
}

